
     // ***************Operator in Js*****************
// Arithmetic[+ , - , * , / , % , **]
// Unary[++ , --]
// Assignment[= , += , -= , *= /= , %= etc.]
// Comparison 
// Logical 

let a = 10;
let b = 5;
console.log(a + b); // Ans. 15
console.log(a - b);  //Ans. 5
console.log(a * b); //Ans. 50
console.log(a / b); //Ans. 2
console.log(a % b); //Ans. 0
console.log(a ** b); //Ans. 100000

console.log(a++);  // Ans. 10
console.log(++a); // Ans. 12

console.log(+a);






